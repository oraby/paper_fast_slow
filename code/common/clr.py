def Age(idx, max_idx, start_color=[0, 238, 255], end_color=[134, 0, 255]):

    STEP = ((start_color[1]-end_color[1])-(start_color[0]-end_color[0]))/max_idx
    color = start_color
    UP_LIM, LOW_LIM = True, False
    remaining = STEP * idx
    for color_idx, is_up_lim in [(1, LOW_LIM), (0, UP_LIM)]:  # , (2, UP_LIM)]:
        color_buffer = (255 - color[color_idx]) if is_up_lim else \
                       color[color_idx]
        while remaining and color_buffer > 0:
            add_val = min(remaining, color_buffer, STEP)
            color[color_idx] += add_val if is_up_lim else -add_val
            color_buffer = (255 - color[color_idx]) if is_up_lim else \
                           color[color_idx]
            remaining -= add_val
        idx -= 1
    return (color[0] / 255, color[1] / 255, color[2] / 255)

def adjustColorLightness(color, amount):
    """Lightens the given color by multiplying (1-luminosity) by the given
    amount. Input can be matplotlib color string, hex string, or RGB tuple.

    Examples:
    >> lighten_color('g', 0.3)
    >> lighten_color('#F034A3', 0.6)
    >> lighten_color((.3,.55,.1), 0.5)

    Copied from: https://stackoverflow.com/a/49601444/11996983
    """
    import matplotlib.colors as mc
    import colorsys

    try:
        c = mc.cnames[color]
    except:
        c = color
    c = colorsys.rgb_to_hls(*mc.to_rgb(c))
    return colorsys.hls_to_rgb(c[0], max(0, min(1, amount * c[1])), c[2])


